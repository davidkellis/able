	fmt.Fprintf(buf, "func __able_interface_dispatch_method(val *runtime.InterfaceValue, name string) (runtime.Value, bool) {\n")
	fmt.Fprintf(buf, "\tif __able_runtime == nil || val == nil || name == \"\" {\n")
	fmt.Fprintf(buf, "\t\treturn nil, false\n")
	fmt.Fprintf(buf, "\t}\n")
	fmt.Fprintf(buf, "\tif val.Methods != nil {\n")
	fmt.Fprintf(buf, "\t\tif cached, ok := val.Methods[name]; ok && cached != nil {\n")
	fmt.Fprintf(buf, "\t\t\tbound := __able_interface_bind_method(val, cached)\n")
	fmt.Fprintf(buf, "\t\t\tif bound != nil {\n")
	fmt.Fprintf(buf, "\t\t\t\tval.Methods[name] = bound\n")
	fmt.Fprintf(buf, "\t\t\t\treturn bound, true\n")
	fmt.Fprintf(buf, "\t\t\t}\n")
	fmt.Fprintf(buf, "\t\t}\n")
	fmt.Fprintf(buf, "\t}\n")
	fmt.Fprintf(buf, "\tifaceName := \"\"\n")
	fmt.Fprintf(buf, "\tif val.Interface != nil && val.Interface.Node != nil && val.Interface.Node.ID != nil {\n")
	fmt.Fprintf(buf, "\t\tifaceName = val.Interface.Node.ID.Name\n")
	fmt.Fprintf(buf, "\t}\n")
	fmt.Fprintf(buf, "\tif ifaceName == \"\" {\n")
	fmt.Fprintf(buf, "\t\treturn nil, false\n")
	fmt.Fprintf(buf, "\t}\n")
	fmt.Fprintf(buf, "\tvar entries []__able_interface_dispatch_entry\n")
	fmt.Fprintf(buf, "\tif perIface := __able_interface_dispatch[ifaceName]; perIface != nil {\n")
	fmt.Fprintf(buf, "\t\tentries = perIface[name]\n")
	fmt.Fprintf(buf, "\t}\n")
	fmt.Fprintf(buf, "\tif len(entries) == 0 {\n")
	fmt.Fprintf(buf, "\t\tnames := __able_interface_search(ifaceName)\n")
	fmt.Fprintf(buf, "\t\tif len(names) > 1 {\n")
	fmt.Fprintf(buf, "\t\t\tfor _, base := range names[1:] {\n")
	fmt.Fprintf(buf, "\t\t\t\tif perIface := __able_interface_dispatch[base]; perIface != nil {\n")
	fmt.Fprintf(buf, "\t\t\t\t\tentries = append(entries, perIface[name]...)\n")
	fmt.Fprintf(buf, "\t\t\t\t}\n")
	fmt.Fprintf(buf, "\t\t\t}\n")
	fmt.Fprintf(buf, "\t\t}\n")
	fmt.Fprintf(buf, "\t}\n")
	fmt.Fprintf(buf, "\tif len(entries) == 0 {\n")
	fmt.Fprintf(buf, "\t\treturn nil, false\n")
	fmt.Fprintf(buf, "\t}\n")
	fmt.Fprintf(buf, "\treceiver := __able_unwrap_interface(val.Underlying)\n")
	fmt.Fprintf(buf, "\tif receiver == nil {\n")
	fmt.Fprintf(buf, "\t\treceiver = runtime.NilValue{}\n")
	fmt.Fprintf(buf, "\t}\n")
	fmt.Fprintf(buf, "\tmatches := make([]__able_interface_dispatch_match, 0, 1)\n")
	fmt.Fprintf(buf, "\tfor _, entry := range entries {\n")
	fmt.Fprintf(buf, "\t\tif len(entry.genericNames) > 0 {\n")
	fmt.Fprintf(buf, "\t\t\tactual, err := bridge.TypeExpressionFromValue(__able_runtime, receiver)\n")
	fmt.Fprintf(buf, "\t\t\tif err != nil {\n")
	fmt.Fprintf(buf, "\t\t\t\t__able_panic_on_error(err)\n")
	fmt.Fprintf(buf, "\t\t\t\treturn nil, false\n")
	fmt.Fprintf(buf, "\t\t\t}\n")
	fmt.Fprintf(buf, "\t\t\tif actual == nil {\n")
	fmt.Fprintf(buf, "\t\t\t\tcontinue\n")
	fmt.Fprintf(buf, "\t\t\t}\n")
	fmt.Fprintf(buf, "\t\t\ttemplate, err := bridge.ExpandTypeAliases(__able_runtime, entry.targetType)\n")
	fmt.Fprintf(buf, "\t\t\tif err != nil {\n")
	fmt.Fprintf(buf, "\t\t\t\t__able_panic_on_error(err)\n")
	fmt.Fprintf(buf, "\t\t\t\treturn nil, false\n")
	fmt.Fprintf(buf, "\t\t\t}\n")
	fmt.Fprintf(buf, "\t\t\tactual, err = bridge.ExpandTypeAliases(__able_runtime, actual)\n")
	fmt.Fprintf(buf, "\t\t\tif err != nil {\n")
	fmt.Fprintf(buf, "\t\t\t\t__able_panic_on_error(err)\n")
	fmt.Fprintf(buf, "\t\t\t\treturn nil, false\n")
	fmt.Fprintf(buf, "\t\t\t}\n")
	fmt.Fprintf(buf, "\t\t\tbindings := make(map[string]ast.TypeExpression)\n")
	fmt.Fprintf(buf, "\t\t\tif !__able_match_type_template(template, actual, entry.genericNames, bindings) {\n")
	fmt.Fprintf(buf, "\t\t\t\tcontinue\n")
	fmt.Fprintf(buf, "\t\t\t}\n")
	fmt.Fprintf(buf, "\t\t\tif len(val.InterfaceArgs) > 0 {\n")
	fmt.Fprintf(buf, "\t\t\t\tif len(entry.interfaceArgs) == 0 || len(entry.interfaceArgs) != len(val.InterfaceArgs) {\n")
	fmt.Fprintf(buf, "\t\t\t\t\tcontinue\n")
	fmt.Fprintf(buf, "\t\t\t\t}\n")
	fmt.Fprintf(buf, "\t\t\t\tifaceOk := true\n")
	fmt.Fprintf(buf, "\t\t\t\tfor idx := range entry.interfaceArgs {\n")
	fmt.Fprintf(buf, "\t\t\t\t\tleft, err := bridge.ExpandTypeAliases(__able_runtime, entry.interfaceArgs[idx])\n")
	fmt.Fprintf(buf, "\t\t\t\t\tif err != nil {\n")
	fmt.Fprintf(buf, "\t\t\t\t\t\t__able_panic_on_error(err)\n")
	fmt.Fprintf(buf, "\t\t\t\t\t\treturn nil, false\n")
	fmt.Fprintf(buf, "\t\t\t\t\t}\n")
	fmt.Fprintf(buf, "\t\t\t\t\tright, err := bridge.ExpandTypeAliases(__able_runtime, val.InterfaceArgs[idx])\n")
	fmt.Fprintf(buf, "\t\t\t\t\tif err != nil {\n")
	fmt.Fprintf(buf, "\t\t\t\t\t\t__able_panic_on_error(err)\n")
	fmt.Fprintf(buf, "\t\t\t\t\t\treturn nil, false\n")
	fmt.Fprintf(buf, "\t\t\t\t\t}\n")
	fmt.Fprintf(buf, "\t\t\t\t\tif !__able_match_type_template(left, right, entry.genericNames, bindings) {\n")
	fmt.Fprintf(buf, "\t\t\t\t\t\tifaceOk = false\n")
	fmt.Fprintf(buf, "\t\t\t\t\t\tbreak\n")
	fmt.Fprintf(buf, "\t\t\t\t\t}\n")
	fmt.Fprintf(buf, "\t\t\t\t}\n")
	fmt.Fprintf(buf, "\t\t\t\tif !ifaceOk {\n")
	fmt.Fprintf(buf, "\t\t\t\t\tcontinue\n")
	fmt.Fprintf(buf, "\t\t\t\t}\n")
	fmt.Fprintf(buf, "\t\t\t}\n")
	fmt.Fprintf(buf, "\t\t\tif !__able_enforce_constraints(entry.constraints, bindings) {\n")
	fmt.Fprintf(buf, "\t\t\t\tcontinue\n")
	fmt.Fprintf(buf, "\t\t\t}\n")
	fmt.Fprintf(buf, "\t\t\tmatches = append(matches, __able_interface_dispatch_match{entry: entry, receiver: receiver})\n")
	fmt.Fprintf(buf, "\t\t\tcontinue\n")
	fmt.Fprintf(buf, "\t\t}\n")
	fmt.Fprintf(buf, "\t\tif len(val.InterfaceArgs) > 0 {\n")
	fmt.Fprintf(buf, "\t\t\tif len(entry.interfaceArgs) == 0 || !__able_type_expr_list_equal(entry.interfaceArgs, val.InterfaceArgs) {\n")
	fmt.Fprintf(buf, "\t\t\t\tcontinue\n")
	fmt.Fprintf(buf, "\t\t\t}\n")
	fmt.Fprintf(buf, "\t\t}\n")
	fmt.Fprintf(buf, "\t\tcoerced, ok, err := bridge.MatchType(__able_runtime, entry.targetType, receiver)\n")
	fmt.Fprintf(buf, "\t\tif err != nil {\n")
	fmt.Fprintf(buf, "\t\t\t__able_panic_on_error(err)\n")
	fmt.Fprintf(buf, "\t\t\treturn nil, false\n")
	fmt.Fprintf(buf, "\t\t}\n")
	fmt.Fprintf(buf, "\t\tif !ok {\n")
	fmt.Fprintf(buf, "\t\t\tcontinue\n")
	fmt.Fprintf(buf, "\t\t}\n")
	fmt.Fprintf(buf, "\t\tif !__able_enforce_constraints(entry.constraints, nil) {\n")
	fmt.Fprintf(buf, "\t\t\tcontinue\n")
	fmt.Fprintf(buf, "\t\t}\n")
	fmt.Fprintf(buf, "\t\tmatches = append(matches, __able_interface_dispatch_match{entry: entry, receiver: coerced})\n")
	fmt.Fprintf(buf, "\t}\n")
	fmt.Fprintf(buf, "\tif len(matches) == 0 {\n")
	fmt.Fprintf(buf, "\t\treturn nil, false\n")
	fmt.Fprintf(buf, "\t}\n")
	fmt.Fprintf(buf, "\tbestIdx := 0\n")
	fmt.Fprintf(buf, "\tcontenders := []int{0}\n")
	fmt.Fprintf(buf, "\tfor idx := 1; idx < len(matches); idx++ {\n")
	fmt.Fprintf(buf, "\t\tcmp := __able_compare_interface_entries(matches[idx].entry, matches[bestIdx].entry)\n")
	fmt.Fprintf(buf, "\t\tif cmp > 0 {\n")
	fmt.Fprintf(buf, "\t\t\tbestIdx = idx\n")
	fmt.Fprintf(buf, "\t\t\tcontenders = []int{idx}\n")
	fmt.Fprintf(buf, "\t\t} else if cmp == 0 {\n")
	fmt.Fprintf(buf, "\t\t\treverse := __able_compare_interface_entries(matches[bestIdx].entry, matches[idx].entry)\n")
	fmt.Fprintf(buf, "\t\t\tif reverse < 0 {\n")
	fmt.Fprintf(buf, "\t\t\t\tbestIdx = idx\n")
	fmt.Fprintf(buf, "\t\t\t\tcontenders = []int{idx}\n")
	fmt.Fprintf(buf, "\t\t\t} else if reverse == 0 {\n")
	fmt.Fprintf(buf, "\t\t\t\tcontenders = append(contenders, idx)\n")
	fmt.Fprintf(buf, "\t\t\t}\n")
	fmt.Fprintf(buf, "\t\t}\n")
	fmt.Fprintf(buf, "\t}\n")
	fmt.Fprintf(buf, "\tif len(contenders) > 1 {\n")
	fmt.Fprintf(buf, "\t\t__able_panic_on_error(fmt.Errorf(\"ambiguous implementations of %%s\", ifaceName))\n")
	fmt.Fprintf(buf, "\t\treturn nil, false\n")
	fmt.Fprintf(buf, "\t}\n")
	fmt.Fprintf(buf, "\tmatch := matches[bestIdx]\n")
	fmt.Fprintf(buf, "\tif match.entry.isPrivate {\n")
	fmt.Fprintf(buf, "\t\t__able_panic_on_error(fmt.Errorf(\"Method '%%s' on %%s is private\", name, ifaceName))\n")
	fmt.Fprintf(buf, "\t\treturn nil, false\n")
	fmt.Fprintf(buf, "\t}\n")
	fmt.Fprintf(buf, "\tbound := &runtime.NativeBoundMethodValue{Receiver: match.receiver, Method: *match.entry.fn}\n")
	fmt.Fprintf(buf, "\tif val.Methods == nil {\n")
	fmt.Fprintf(buf, "\t\tval.Methods = map[string]runtime.Value{}\n")
	fmt.Fprintf(buf, "\t}\n")
	fmt.Fprintf(buf, "\tval.Methods[name] = bound\n")
	fmt.Fprintf(buf, "\treturn bound, true\n")
	fmt.Fprintf(buf, "}\n\n")
