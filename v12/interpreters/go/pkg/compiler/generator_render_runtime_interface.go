	fmt.Fprintf(buf, "func __able_type_expr_has_unknown_names(expr ast.TypeExpression) bool {\n")
	fmt.Fprintf(buf, "\tswitch t := expr.(type) {\n")
	fmt.Fprintf(buf, "\tcase *ast.SimpleTypeExpression:\n")
