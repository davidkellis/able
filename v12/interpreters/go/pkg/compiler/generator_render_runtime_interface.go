	fmt.Fprintf(buf, "\t\t\tconstraints:    constraints,\n")
	fmt.Fprintf(buf, "\t\t\tconstraintKeys: constraintKeys,\n")
	fmt.Fprintf(buf, "\t\t\tunionVariants:  unionSigs,\n")
